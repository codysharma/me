(function() {
  "use strict";

  /**
   * Easy selector helper function
   */
  const select = (element, all = false) => {
    element = element.trim()
    if (all) {
      return [...document.querySelectorAll(element)]
    } else {
      return document.querySelector(element)
    }
  }

  /**
   * Easy event listener function
   */
  const on = (type, element, listener, all = false) => {
    let selectEl = select(element, all)
    if (selectEl) {
      if (all) {
        selectEl.forEach(e => e.addEventListener(type, listener))
      } else {
        selectEl.addEventListener(type, listener)
      }
    }
  }

  /**
   * Easy on scroll event listener 
   */
  const onscroll = (element, listener) => {
    element.addEventListener('scroll', listener)
  }

  /**
   * Navbar links active state on scroll
   */
  let navbarlinks = select('#navbar .scrollto', true)
  const navbarlinksActive = () => {
    let position = window.scrollY + 200
    navbarlinks.forEach(navbarlink => {
      if (!navbarlink.hash) return
      let section = select(navbarlink.hash)
      if (!section) return
      if (position >= section.offsetTop && position <= (section.offsetTop + section.offsetHeight)) {
        navbarlink.classList.add('active')
      } else {
        navbarlink.classList.remove('active')
      }
    })
  }
  window.addEventListener('load', navbarlinksActive)
  onscroll(document, navbarlinksActive)

  /**
   * Scrolls to an element with header offset
   */
  const scrollto = (el) => {
    let elementPos = select(el).offsetTop
    window.scrollTo({
      top: elementPos,
      behavior: 'smooth'
    })
  }

  /**
   * Back to top button
   */
  let backtotop = select('.back-to-top')
  if (backtotop) {
    const toggleBacktotop = () => {
      if (window.scrollY > 100) {
        backtotop.classList.add('active')
      } else {
        backtotop.classList.remove('active')
      }
    }
    window.addEventListener('load', toggleBacktotop)
    onscroll(document, toggleBacktotop)
  }

  /**
   * Mobile nav toggle
   */
  on('click', '.mobile-nav-toggle', function(e) {
    select('body').classList.toggle('mobile-nav-active')
    this.classList.toggle('bi-list')
    this.classList.toggle('bi-x')
  })

  /**
   * Scrool with ofset on links with a class name .scrollto
   */
  on('click', '.scrollto', function(e) {
    if (select(this.hash)) {
      e.preventDefault()

      let body = select('body')
      if (body.classList.contains('mobile-nav-active')) {
        body.classList.remove('mobile-nav-active')
        let navbarToggle = select('.mobile-nav-toggle')
        navbarToggle.classList.toggle('bi-list')
        navbarToggle.classList.toggle('bi-x')
      }
      scrollto(this.hash)
    }
  }, true)

  /**
   * Scroll with ofset on page load with hash links in the url
   */
  window.addEventListener('load', () => {
    if (window.location.hash) {
      if (select(window.location.hash)) {
        scrollto(window.location.hash)
      }
    }
  });

  /**
   * Preloader
   */
  let preloader = select('#preloader');
  if (preloader) {
    window.addEventListener('load', () => {
      preloader.remove()
    });
  }

  /**
   * Hero type effect
   */
  const typed = select('.typed')
  if (typed) {
    let typed_strings = typed.getAttribute('data-typed-items')
    typed_strings = typed_strings.split(',')
    new Typed('.typed', {
      strings: typed_strings,
      loop: true,
      typeSpeed: 100,
      backSpeed: 50,
      backDelay: 2000
    });
  }

  /**
   * Skills animation
   */
  let skilsContent = select('.skills-content');
  if (skilsContent) {
    new Waypoint({
      element: skilsContent,
      offset: '80%',
      handler: function(direction) {
        let progress = select('.progress .progress-bar', true);
        progress.forEach((el) => {
          el.style.width = el.getAttribute('aria-valuenow') + '%'
        });
      }
    })
  }

  /**
   * Porfolio isotope and filter
   */
  window.addEventListener('load', () => {
    let portfolioContainer = select('.portfolio-container');
    if (portfolioContainer) {
      let portfolioIsotope = new Isotope(portfolioContainer, {
        itemSelector: '.portfolio-item'
      });

      let portfolioFilters = select('#portfolio-flters li', true);

      on('click', '#portfolio-flters li', function(e) {
        e.preventDefault();
        portfolioFilters.forEach(function(el) {
          el.classList.remove('filter-active');
        });
        this.classList.add('filter-active');

        portfolioIsotope.arrange({
          filter: this.getAttribute('data-filter')
        });
        portfolioIsotope.on('arrangeComplete', function() {
          AOS.refresh()
        });
      }, true);
    }

  });

  /**
   * Initiate portfolio lightbox 
   */
  const portfolioLightbox = GLightbox({
    selector: '.portfolio-lightbox'
  });

  /**
   * Initiate portfolio details lightbox 
   */
  const portfolioDetailsLightbox = GLightbox({
    selector: '.portfolio-details-lightbox',
    width: '90%',
    height: '90vh'
  });

  /**
   * Portfolio details slider
   */
  new Swiper('.portfolio-details-slider', {
    speed: 400,
    loop: true,
    autoplay: {
      delay: 5000,
      disableOnInteraction: false
    },
    pagination: {
      el: '.swiper-pagination',
      type: 'bullets',
      clickable: true
    }
  });

  /**
   * Testimonials slider
   */
  new Swiper('.testimonials-slider', {
    speed: 600,
    loop: true,
    autoplay: {
      delay: 5000,
      disableOnInteraction: false
    },
    slidesPerView: 'auto',
    pagination: {
      el: '.swiper-pagination',
      type: 'bullets',
      clickable: true
    }
  });

  /**
   * Animation on scroll
   */
  window.addEventListener('load', () => {
    AOS.init({
      duration: 1000,
      easing: 'ease-in-out',
      once: true,
      mirror: false
    })
  });

  /**
   * Initiate Pure Counter 
   */
  new PureCounter();

})()

// making bg of landing section a slideshow instead of static
const landingBackgrounds = [
  '../assets/img/landingImages/4.png',
  '../assets/img/landingImages/1.png',
  '../assets/img/landingImages/2.png',
  '../assets/img/landingImages/3.png',
  
]

var currentIndex = 0;
var activeBg = 'bg1';

function nextBackground() {
  const current = document.getElementById(activeBg);
  const next = document.getElementById(activeBg === 'bg1' ? 'bg2' : 'bg1');
  
  // Set the next background image
  next.style.backgroundImage = `url(${landingBackgrounds[currentIndex]})`;
  
  // Toggle active class for fade effect
  current.classList.remove('active');
  next.classList.add('active');
  
  // Toggle which element is active
  activeBg = activeBg === 'bg1' ? 'bg2' : 'bg1';
  
  // Increment index - this is the key part
  currentIndex = (currentIndex + 1) % landingBackgrounds.length;
}

// Initialize first background
document.getElementById('bg1').style.backgroundImage = `url(${landingBackgrounds[0]})`;
document.getElementById('bg1').classList.add('active');

setInterval(nextBackground, 3500); 

// -------------Tableau Section hide/display
let tableauSection = document.getElementById('tableau-content');
let tableauHeading = document.getElementById('tableau-heading');
let tableauToggleDown = document.getElementById('tableauToggleDown');
let tableauToggleUp = document.getElementById('tableauToggleUp');

tableauHeading.addEventListener('click', function(){
  toggleDisplay(tableauSection, tableauToggleDown, tableauToggleUp);
})

function toggleDisplay(section, tableauToggleDown, tableauToggleUp) {
  // Toggle the class between visible and not
  section.classList.toggle('visible');
  
  // Change the arrows visible in header based on section visibility
  if (section.classList.contains('visible')) {
    tableauToggleDown.style.display = 'none';
    tableauToggleUp.style.display = 'inline';
  } else {
    tableauToggleDown.style.display = 'inline';
    tableauToggleUp.style.display = 'none';
  }
}